function insertModel(model, x, y) {
  modelRowCounter = 0;

  while (modelRowCounter < (model.length)){
    var currentY = modelRowCounter + y;
    var currentDiv = "row" + currentY.toString()

    var currentRow = document.getElementById(currentDiv).innerHTML;
    currentRow = symbolToCharacter(currentRow);
    currentRow = stringSplice(currentRow, Math.round(x) , model[modelRowCounter].length, model[modelRowCounter]);
    currentRow = characterToSymbol(currentRow);
    document.getElementById(currentDiv).innerHTML = currentRow;
    // console.log(modelRowCounter);
    modelRowCounter = modelRowCounter + 1;

  }
}
function symbolToCharacter (string){
  // this function removes the html code for spaces and replaces it with a string  space
  var counter = 0;
  var countToRemove = 0;
  var newString = string;
  var returnString = "";
  while (counter < (string.length)){

    if (string[counter] == "&"){
      if (string[counter + 1] == "n"){
        if (string[counter + 2] == "b"){
          if (string[counter + 3] == "s"){
            if (string[counter + 4] == "p"){
              if (string[counter + 5] == ";"){
                returnString = returnString + " ";
                counter = counter + 5;
              }

            }

          }
        }
      }
    }
    else {
      returnString = returnString + string[counter];
    }

    counter = counter + 1;
  }
  return (returnString);
}
function characterToSymbol (string){
  var returnString = "";
  var newString = string;
  var counter = 0;
  while (counter < string.length){
    if (string[counter] == " ") {
      returnString = returnString + "&nbsp;";
    }
    else {
      returnString = returnString + string[counter];
    }
    counter = counter + 1;
  }

  return (returnString);
}
function stringSplice(string, start, removeCount, insert) {
  // removes a number of characters specified by you from a start point and inserts a string given by you
  // this is coded quite lasily therefore you have to remove a character and can't just insert something
  newString = string;
  returnString = "";
  indexCounter = 0;
  while (indexCounter < newString.length){
    if (indexCounter >= start && indexCounter <= (start + removeCount - 1)) {
      if (indexCounter == start){
        returnString = returnString + insert;
      }
    }
    else {
      returnString = returnString + newString[indexCounter];
    }
    indexCounter = indexCounter + 1;
  }
  return (returnString);

}
